/// <summary>
/// Stat types of items.
/// </summary>
public enum StatType
{
    AdditionalAttacksProbability,       // 추가 공격 확률
    Attack,                             // 공격력
    AttackPowerMultiplier,              // 공격력 배수
    AttackPowerMultiplierProbability,   // 공격력 배수 확률
    AttackRange,                        // 사거리
    AttackSpeed,                        // 공격 속도 (연사력)
    Branche,                            // 갈래
    BulletSize,                         // 총알 크기
    BulletSpeed,                        // 총알 속도
    BulletSpread,                       // 총알 퍼짐
    CriticalProbability,                // 치명타 확률
    Defense,                            // 방어력
    ExplosionRange,                     // 폭발 범위
    GrenadeProbability,                 // 고폭유탄 발사 확률
    Health,                             // 체력
    MagazineSize,                       // 탄창 크기
    MaxAdditionalAttacks,               // 추가 공격 최대 횟수
    MoveSpeed,                          // 이동 속도
    NoReloadTimeProbability,            // 재장전 시간 없음 확률
    PenetratingPower,                   // 관통력
    PenetratingProbability,             // 관통 확률
    ReloadTime,                         // 재장전 시간
    GunID
}
